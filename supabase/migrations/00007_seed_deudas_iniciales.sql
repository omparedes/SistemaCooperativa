insert into public.montos_por_cobrar
  (puesto_id, concepto_id, periodo_anio, periodo_mes,
   monto, estado, metodo_calculo, fecha_generacion)
select ha.puesto_id, c.id, 2025, 10,
       s.monto_asignado, 'Pendiente', 'Fijo', '2025-10-31'
from source_agua_fija s
join public.inquilinos inq
  on inq.apellidos = s.nombre_display and inq.deleted_at is null
join public.historial_arriendos ha
  on ha.inquilino_id = inq.id and ha.fecha_fin is null
join public.conceptos c on c.nombre = 'Agua' and c.deleted_at is null
on conflict (puesto_id, concepto_id, periodo_anio, periodo_mes)
  where deleted_at is null and puesto_id is not null do nothing;

